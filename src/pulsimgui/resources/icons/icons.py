def get_icon_svg(name: str, color: str = "#000000", size: int = 24) -> str:
    """Get an icon SVG with the specified color.

    Args:
        name: Icon name from ICONS dict
        color: Hex color string for the stroke
        size: Icon size in pixels

    Returns:
        SVG string with color applied
    """
    svg = ICONS.get(name, ICONS["help-circle"])
    # Replace currentColor with the specified color
    svg = svg.replace('stroke="currentColor"', f'stroke="{color}"')
    # Update size if needed
    if size != 24:
        svg = svg.replace('width="24"', f'width="{size}"')
        svg = svg.replace('height="24"', f'height="{size}"')
    return svg


def get_available_icons() -> list[str]:
    """Get list of available icon names."""
    return list(ICONS.keys())
